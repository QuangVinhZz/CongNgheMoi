    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        clsapi cls = new clsapi();
    
        int selectedRow = tblsinhvien.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một sinh viên để cập nhật!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String id = tblsinhvien.getValueAt(selectedRow, 0).toString();
        // Lấy dữ liệu từ JTextField
        String mssv = txtmssv.getText().trim();
        String hodem = txtho.getText().trim();
        String ten = txtten.getText().trim();
        String lop = txtlop.getText().trim();

        // Mã hóa URL (thay khoảng trắng bằng %20)
        mssv = mssv.replaceAll(" ", "%20");
        hodem = hodem.replaceAll(" ", "%20");
        ten = ten.replaceAll(" ", "%20");
        lop = lop.replaceAll(" ", "%20");

        // Tạo URL để gọi API sửa sinh viên
        String thamso = "id=" + id + "&mssv=" + mssv + "&hodem=" + hodem + "&ten=" + ten + "&lop=" + lop;
        String myurl = "http://localhost/Test2/api/suasv.php?" + thamso;

        cls.geturl(myurl);
        loadtable();
        xoatext();
    }//GEN-LAST:event_btnSuaActionPerformed

