    private void loadtable(){
        try{
            clsapi cls = new clsapi();
            JSONArray jarr = new JSONArray();
            jarr = cls.docapi();
            Vector<Vector<String>> datalist=new Vector<>();
            for(int i=0;i<jarr.length();i++){
                JSONObject job=jarr.getJSONObject(i);
                Vector<String> data=new Vector<>();
                data.add(job.getString("id"));
                data.add(job.getString("mssv"));
                data.add(job.getString("hodem"));
                data.add(job.getString("ten"));
                data.add(job.getString("lop"));
                datalist.add(data);
            }
            Vector<String> column=new Vector<>();
            column.add("ID");
            column.add("Ma sinh vien");
            column.add("Ho dem");
            column.add("Ten");
            column.add("Lop");
            DefaultTableModel model = new DefaultTableModel(datalist,column);
            tblsinhvien.setModel(model);
            
        }catch(Exception e){
            
        }
    }
