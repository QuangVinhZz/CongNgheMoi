    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        clsapi cls = new clsapi();

        int selectedRow = tblsinhvien.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một sinh viên để xóa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String id = tblsinhvien.getValueAt(selectedRow, 0).toString();
        System.out.println(id);

        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa sinh viên có ID: " + id + "?", "Xác nhận", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            id = id.replaceAll(" ", "%20");
            String myurl = "http://localhost/Test2/api/xoasv.php?id=" + id;
            cls.geturl(myurl);
            loadtable();
            xoatext();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

